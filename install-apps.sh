#!/bin/bash

#---------------------------------------------------------------------------------------------------
#  Add Kali repository to /etc/apt/sources.list                                                   /
#---------------------------------------------------------------------------------------------------

echo deb http://http.kali.org/kali kali-rolling main non-free contrib >> /etc/apt/sources.list

#--------------------------------------------------------------------------------------------------
#  Update sources                                                                                 /
#--------------------------------------------------------------------------------------------------

apt update -y
apt upgrade -y

#-------------------------------------------------------------------------------------------------
#  Install apps                                                                                  /
#-------------------------------------------------------------------------------------------------

apt install -y \
  terminator \
  ftp \
  exiftool \
  hurl \
  veil \
  kali-linux-sdr \
  gpsd gpsd-clients \
